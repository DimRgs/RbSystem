package system.po;

public class Wssm {
	private int id;
	private int rb_id;
	private String note;
	private String stamp_pic;
	private String special_pic;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getStamp_pic() {
		return stamp_pic;
	}
	public void setStamp_pic(String stamp_pic) {
		this.stamp_pic = stamp_pic;
	}
	public String getSpecial_pic() {
		return special_pic;
	}
	public void setSpecial_pic(String special_pic) {
		this.special_pic = special_pic;
	}
	public int getRb_id() {
		return rb_id;
	}
	public void setRb_id(int rb_id) {
		this.rb_id = rb_id;
	}
}
